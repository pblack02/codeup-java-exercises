public class StringExercise {
    public static void main(String[] args) {
        String inputOne = "We don't need no education";
        System.out.println(inputOne);
        String inputTwo = "We don't need no thought control";
        System.out.println(inputTwo);
        String inputThree = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(inputThree);
        String inputFour = "In windows, the main drive is usually C:\\\n";
        System.out.println(inputFour);
        String inputFive = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!\n";
        System.out.println(inputFive);


    }
}
